package operators;

public class conditionalOperator {

	public static void main(String[] args) {
		int A=20;
		int B=30;
		if((A==20) && (B==30)) {
		System.out.println("Above AND operation is true");
		}
		if((A==30) || (B==30)) {
		System.out.println("Above OR operation is true");
		}

	}

}
