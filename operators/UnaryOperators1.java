package operators;

public class UnaryOperators1 {

	public static void main(String[] args) {
		int bindu=40;
		System.out.println("print"+bindu);
		++bindu;
		System.out.println("after inc"+bindu);
		bindu--;
		System.out.println("after dec"+bindu);
		
		boolean hima=true;
		System.out.println(hima);
		System.out.println(!hima);

	}

}
